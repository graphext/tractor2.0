    <h2 class="">Information about APIFY Auth Tokens</h2>
    <p>
        In order to use this service, please head to <a
            href="https://console.apify.com/settings/integrations"
            target="_blank">the APIFY Integrations</a
        >
        section and generate a personal API token.
    </p>
    <p>
        You'll find it at the very right, inside the "Personal API Tokens"
        section. A button labelled <span
            class="text-primary px-1 border border-primary rounded-btn"
            >+ Add token</span
        >.
    </p>

    <p>
        As stated, do not share this with untrusted sources.
        <b
            >If you notice unwanted or unusual behaviour, head to the same page
            and delete all affected tokens. Delete them all if you are not sure
            which one is being misused.</b
        > You can always generate new ones.
    </p>

    <h3>How this works</h3>
    <p>
        The APIFY service always asks for a kind of identification number for
        each user. When using integrations that work on your behalf, you can
        provide this token so, even though you are using a service external to
        APIFY (like this one), APIFY knows it's <b>you</b> the one using it and can,
        for example, charge you for the service provided.
    </p>

    <p>
        Basically, you are just using APIFY in a more convenient way, since we
        fill all the forms and press all the buttons for you. But it's you, in
        the end, the one using the APIFY service.
    </p>
